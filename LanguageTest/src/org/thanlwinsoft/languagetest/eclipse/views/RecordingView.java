package org.thanlwinsoft.languagetest.eclipse.views;

import org.eclipse.swt.SWT;
import org.eclipse.swt.layout.FillLayout;
import org.eclipse.swt.widgets.Composite;
import org.eclipse.ui.part.ViewPart;
import org.thanlwinsoft.eclipse.widgets.SoundRecorder;

/**
 * @author keith
 *
 */
public class RecordingView extends ViewPart
{
    private SoundRecorder recorder = null; 
    public final static String ID = "org.thanlwinsoft.languagetest.eclipse.views.RecordingView";
    /* (non-Javadoc)
     * @see org.eclipse.ui.part.WorkbenchPart#createPartControl(org.eclipse.swt.widgets.Composite)
     */
    public void createPartControl(Composite parent)
    {
        parent.setLayout(new FillLayout());
        recorder = new SoundRecorder(parent, SWT.NONE);
    }

    /* (non-Javadoc)
     * @see org.eclipse.ui.part.WorkbenchPart#setFocus()
     */
    public void setFocus()
    {
        recorder.setFocus();
    }
    public SoundRecorder getRecorder()
    {
        return recorder;
    }
}
